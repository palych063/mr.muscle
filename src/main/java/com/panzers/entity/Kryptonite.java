package com.panzers.entity;

import javax.persistence.*;

@Entity
@Table(name="test")
public class Kryptonite {

	@Id
	@GeneratedValue(strategy=GenerationType.IDENTITY)
	public long id;

	public String name;

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
